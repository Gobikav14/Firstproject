package practice;

public class Task7 {
	public static void main(String[] args) {
		String a=args[0];
		char[]b=a.toCharArray();
		Arrays.sort(b);
		for(int i=0;i<=b.length-1;i++) {
			String text;
			if(i!=b.length-1) {
		text=b[i]+" ";
			}
			else {
				text=""+b[i];
			}
			System.out.println(text);
			}
		}
		
	}


